#include <bits/stdc++.h>

using namespace std;

int main()
{
ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--)
    {
        int x;
        cin >> x;
        if (x == 67)
            cout << 67 << '\n';
        else
        {
            cout << x + 1 << '\n';
        }
    }
    return 0;
}